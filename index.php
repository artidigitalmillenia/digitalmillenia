    var currentImage = 1; // Start with the first image
var scrollInterval;

function showContent(imageNumber) {
    // Hide all tab contents
    var tabContents = document.querySelectorAll('.tab-content');
    tabContents.forEach(function (content) {
        content.style.display = 'none';
    });

    // Show the selected tab content
    var selectedContent = document.getElementById('content' + imageNumber);
    selectedContent.style.display = 'block';

    // Remove active class from all images
    var images = document.querySelectorAll('.img-testimonial');
    images.forEach(function (img) {
        img.classList.remove('active');
    });

    // Add active class to the selected image
    var selectedImage = document.querySelector('.img-testimonial:nth-child(' + imageNumber + ')');
    selectedImage.classList.add('active');

    // Stop the previous auto-scrolling interval
    clearInterval(scrollInterval);

    // Set a new interval to resume auto-scrolling after 3 seconds
    setTimeout(function () {
        scrollInterval = setInterval(automateScroll, 3000);
    }, 3000);
}

function automateScroll() {
    showContent(currentImage);

    // Increment the current image number
    currentImage++;

    // Reset to the first image if it exceeds the total number of images
    if (currentImage > 5) {
        currentImage = 1;
    }
}

// Start the initial auto-scrolling interval
scrollInterval = setInterval(automateScroll, 3000); // Set the interval time in milliseconds (adjust as needed)

